BASE_DIR = "initial_data"
LOCAL_DIR = "local"
URLS_FILE = "urls.txt"

# ---- args ----
parser = argparse.ArgumentParser()
parser.add_argument(
    "--no-url",
    action="store_true",
    help="Skip downloading URLs, process only local files"
)
args = parser.parse_args()

# ---- reset workspace safely ----
if os.path.exists(BASE_DIR):
    subprocess.run(["rm", "-rf", BASE_DIR], check=True)

os.makedirs(BASE_DIR, exist_ok=True)

# ---- download files (optional) ----
if not args.no_url:
    subprocess.run(
        ["wget", "-i", f"../{URLS_FILE}"],
        cwd=BASE_DIR,
        check=True
    )
else:
    print("[*] --no-url enabled: skipping URL downloads")

# ---- copy local user files ----
if os.path.isdir(LOCAL_DIR):
    for name in os.listdir(LOCAL_DIR):
        src = os.path.join(LOCAL_DIR, name)
        dst = os.path.join(BASE_DIR, name)

        if not os.path.isfile(src):
            continue

        # prevent overwrite
        if os.path.exists(dst):
            dst = os.path.join(BASE_DIR, f"local_{name}")

        shutil.copy2(src, dst)

print("Decrypting")

# ---- decrypt base64 files ----
for name in os.listdir(BASE_DIR):
    path = os.path.join(BASE_DIR, name)

    if not os.path.isfile(path):
        continue

    decrypted = f"{path}.de"
    b64d.runner(input_file=path, output_file=decrypted)

print("Checking state")

# ---- TCP check (nc) ----
for name in os.listdir(BASE_DIR):
    path = os.path.join(BASE_DIR, name)

    if not path.endswith(".de"):
        continue

    alive = f"{path}.alive"
    subprocess.run(
        ["./nc_test.sh", path, alive],
        check=True
    )

print("Pinging")

# ---- latency filter ----
for name in os.listdir(BASE_DIR):
    path = os.path.join(BASE_DIR, name)

    if not path.endswith(".alive"):
        continue

    filtered = f"{path}.filtered"
    ping_test.filter_by_ping(
        input_file=path,
        output_file=filtered
    )
    os.system(f"wc -l {path}.filtered")


print("Done ✔")
